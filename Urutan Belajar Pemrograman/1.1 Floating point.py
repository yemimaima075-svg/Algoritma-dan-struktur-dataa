# contoh_float_1_modifikasi.py

a = 10.5
b = 3.2

print("Nilai a:", a)
print("Nilai b:", b)

# operasi dasar
print("Penjumlahan:", a + b)
print("Pengurangan:", a - b)
print("Perkalian:", a * b)
print("Pembagian:", a / b)

# tambahan fitur
print("Pembagian (dibulatkan 2 angka):", round(a / b, 2))
print("Rata-rata:", (a + b) / 2)
print("Nilai terbesar:", max(a, b))
print("Nilai terkecil:", min(a, b))
print("Selisih absolut:", abs(a - b))
